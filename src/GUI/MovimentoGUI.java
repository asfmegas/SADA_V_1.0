    private void listarMov() throws SQLException{
        MovimentoDAO dao = new MovimentoDAO();
        listaMov = dao.pesquisar(getCampo(),"%"+jtPesquisa.getText()+"%");
        moastrarPesquisa(listaMov);
    }
//"ID","NOME","NASCIMENTO","CURSO","DISCIPLINA","NOTA 1","NOTA 2","NOTA 3","FALTAS"
    private void moastrarPesquisa(List<Movimento> mov) {
        while(tmMovimento.getRowCount() > 0){
            tmMovimento.removeRow(0);
        }
        if(mov.isEmpty()){
            JOptionPane.showMessageDialog(null, "Sua lista está vazia!");
        }else{
            String[] linhas = new String[]{null,null,null,null,null,null,null,null};
            for(int i=0; i < mov.size();i++){
                tmMovimento.addRow(linhas);
                tmMovimento.setValueAt(mov.get(i).getId(), i, 0);
                tmMovimento.setValueAt(mov.get(i).getNome(), i, 1);
                tmMovimento.setValueAt(formatarDataCampo(mov.get(i).getData()), i, 2);
                tmMovimento.setValueAt(mov.get(i).getNomeCurso(), i, 3);
                tmMovimento.setValueAt(mov.get(i).getNomeDisc(), i, 4);
                tmMovimento.setValueAt(mov.get(i).getNota1(), i, 5);
                tmMovimento.setValueAt(mov.get(i).getNota2(), i, 6);
                tmMovimento.setValueAt(mov.get(i).getNota3(), i, 7);
                tmMovimento.setValueAt(mov.get(i).getFaltas(), i, 8);
            }
        }
    }
    
