    private String getCampo(){
        String campo = "";
        if(jrAluno.isSelected()){
            campo = "a.nome";
        }else if(jrCurso.isSelected()){
            campo = "c.especificacao";
        }else if(jrDisc.isSelected()){
            campo = "d.especificacao";
        }
        return campo;
    }
    /**
     * Formatacao de double para duas casas decimais retirada do forum
     * http://www.guj.com.br/java/296574-restringir-double-para-apenas-duas-casas-decimais
     * acessado em 24-09-2014
     * @return 
     */
    private double calcularMedia(){
        try{
            double nota1 = Double.parseDouble(jtNota1.getText());
            double nota2 = Double.parseDouble(jtNota2.getText());
            double nota3 = Double.parseDouble(jtNota3.getText());
            double media = (nota1+nota2+nota3)/3;
            return Double.valueOf(String.format(Locale.US, "%.2f", media));
        
        }catch(NumberFormatException e){
            //JOptionPane.showMessageDialog(null, "Erro fatal.\n"+e.getMessage());
            return 0;
        }
    }
    
    private int calcularFaltas(){
        int faltasCadastradas = 0;
        int faltasAcrescentadas = 0;
        if(!jtFaltas.equals("")){
            faltasCadastradas = Integer.parseInt(jtFaltas.getText());
            faltasAcrescentadas = Integer.parseInt(jtRFaltas.getText());
        }
        return faltasCadastradas + faltasAcrescentadas;
    }
    
    private void alterarMov(){
        Movimento mov = new Movimento();
        MovimentoDAO dao = new MovimentoDAO();
        mov.setIdMov(idmov);
        mov.setNota1(Double.parseDouble(jtNota1.getText()));
        mov.setNota2(Double.parseDouble(jtNota2.getText()));
        mov.setNota3(Double.parseDouble(jtNota3.getText()));
        mov.setFaltas(calcularFaltas());
        dao.alterar(mov);
        jtFaltas.setText("0");
        limparCampos();
        try {
            listarMov();
        } catch (SQLException ex) {
            Erros.erroSql("Erro ao tentar alterar dados!\n"+ex.getMessage());
        }
        jtMedia.setText("");
    }
    
    private void excluirMov(){
        MovimentoDAO dao = new MovimentoDAO();
        dao.excluir(idmov);
        jtFaltas.setText("0");
        limparCampos();
        try {
            listarMov();
        } catch (SQLException ex) {
            Erros.erroSql("Erro ao tentar excluir dados!\n"+ex.getMessage());
        }
        jtMedia.setText("");
    }
    
    private String formatarDataCampo(String data){
        char[] num = new char[data.length()];
        for(int i=0; i < data.length(); i++){
            num[i] = data.charAt(i);
        }
        String dia = ""+num[8]+num[9];
        String mes = ""+num[5]+num[6];
        String ano = ""+num[0]+num[1]+num[2]+num[3];
        String d = dia+"/"+mes+"/"+ano;
            //1983-02-18
            //0 1 2 3 - 5 6 - 8 9
        return d;
    }
    
